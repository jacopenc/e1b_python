
# Operaciones básicas
a = 10
b = 3

suma = a + b        # 13
resta = a - b       # 7
multiplicacion = a * b   # 30
division = a / b    # 3.333...
division_entera = a // b  # 3
modulo = a % b      # 1 (resto)
potencia = a ** b   # 1000

# Mostrar resultados
print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multiplicacion)
print("División:", division)
print("División entera:", division_entera)
print("Módulo:", modulo)
print("Potencia:", potencia)

