# Ejemplo con variables y diferentes tipos de datos

# Pedimos datos al usuario
nombre = input("Escriba su nombre ")          # str (texto)
edad = int(input("Dime tu edad "))     # int (número entero)
altura = float(input("Indica la altura "))  # float (número decimal)
estudiante = input("¿Eestudias? (sí/no): ")  # str (texto)
print("hola")
# Mostramos los datos

print("Nombre:", nombre)
print("Edad:", edad, "años")
print("Altura:", altura, "m")
print("Estudiante:", estudiante)
